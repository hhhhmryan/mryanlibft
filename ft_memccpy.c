#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;
	unsigned char	*q;
	void			*res;

	i = 0;
	p = (unsigned char *)dst;
	q = (unsigned char *)src;
	while (i < n)
	{
		p[i] = q[i];
		if (p[i] == (unsigned char)c)
		{
			res = &p[i + 1];
			return ((void *)res);
		}
		i++;
	}
	return (NULL);
}
