#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*res;

	i = 0;
	res = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			res = (char *)&s[i];
		}
		i++;
	}
	if (c == '\0')
	{
		res = (char *)&s[i];
	}
	return (res);
}
